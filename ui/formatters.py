def _frame(rows: list[dict]) -> pd.DataFrame:
    return pd.DataFrame(rows or [])


def _join_authors(value: object) -> str:
    if isinstance(value, list):
        return ", ".join(str(item) for item in value if item)
    return str(value or "")


